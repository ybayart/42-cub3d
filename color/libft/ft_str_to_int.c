#include "libft.h"

int		ft_str_to_int(char *str)
{
	int		i;
	int		n;

	i = 0;
	n = 0;
	while (ft_isdigit(str[i]))
	{
		n = (n * 10) + (str[i] - '0');
		i++;
	}
	if (str[i] != '\0')
		return (-1);
	return (n);
}
